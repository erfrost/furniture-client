export const addressFormatToText = (address) => {
  if (address === "kuzovatkina3") {
    return "Г. Нижневартовск МЦ Дом, Ул. Кузоваткина 3, стр. 9";
  } else if (address === "neftyanikov87") {
    return "Г. Нефтеюганск Ул. Нефтяников, 87";
  } else if (address === "mira7") {
    return "Г. Лангепас Отдел Кухонька, Ул. Мира, 7";
  }
};
